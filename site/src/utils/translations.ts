export type Language = keyof typeof translations;

export function t(
  language: Language,
  key: string,
  ...args: any[]
): string {
  const keys = key.split('.');
  let value: any = translations[language];

  for (const k of keys) {
    if (value && typeof value === 'object') {
      value = value[k];
    } else {
      return key; // Return key if translation not found
    }
  }

  if (typeof value === 'function') {
    return value(...args);
  }

  return value || key;
}
